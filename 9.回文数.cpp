#include <vector>
class Solution {
public:
    bool isPalindrome(int x) {
        // if(x < 0) return false;
        // std::vector<int> v;
        // while(x != 0){
        //     v.push_back(x % 10);
        //     x /= 10;
        // }
        // int i = 0;
        // int j = v.size() - 1;
        // while(i < j){
        //     if (v.at(i++) != v.at(j--)) return false;
        // }
        // return true;
        if(x<0|| (x!=0 &&x%10==0)) return false;
        int sum=0;
        while(x>sum)
        {
            sum = sum*10+x%10;
            x = x/10;
        }
        return (x==sum)||(x==sum/10);
    }
};
